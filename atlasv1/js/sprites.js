// ---------------------------------------------------------------------------
// Tier 1 : les 6 exercices piliers reçoivent des frames intermédiaires
// interpolées entre les squelettes-clés. Avec le ping-pong, chaque boucle
// joue 8+ frames — le mouvement se lit, pas juste ses extrêmes.
// ---------------------------------------------------------------------------

const TIER1 = ['dips', 'pullup', 'chinup', 'bench', 'squat', 'hinge', 'legcurl'];

function midSeg(a, b) { return a.map((v, i) => Math.round((v + b[i]) / 2)); }

function midFrame(f1, f2) {
  if (!f1.h || !f2.h) return null;
  const out = { h: midSeg(f1.h, f2.h), dur: 300 };
  for (const k of ['b', 'e', 'o']) {
    const A = f1[k] || [], B = f2[k] || [];
    if (A.length !== B.length) return null;
    if (A.length) out[k] = A.map((s, i) => midSeg(s, B[i]));
  }
  const R1 = f1.r || [], R2 = f2.r || [];
  if (R1.length !== R2.length) return null;
  if (R1.length) out.r = R1.map((r, i) => [...midSeg(r.slice(0, 4), R2[i].slice(0, 4)), r[4]]);
  return out;
}

for (const name of TIER1) {
  const sprite = SPRITES[name];
  const frames = [];
  for (let i = 0; i < sprite.frames.length; i++) {
    frames.push(sprite.frames[i]);
    if (i < sprite.frames.length - 1) {
      const m = midFrame(sprite.frames[i], sprite.frames[i + 1]);
      if (m) frames.push(m);
    }
  }
  sprite.frames = frames;
}
