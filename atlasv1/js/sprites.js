// ATLAS — le système pixel « figure d'amphore ».
// Silhouette terre cuite sur fond noir, vue de profil, grille 32×32.
// Chaque pose est un squelette (segments) ; le rendu est rasterisé au runtime.
// Le tempo de la boucle = le tempo prescrit (mollets : 2 s en bas, réellement).

export const PX = {
  terre: '#C9622B',
  ocre: '#E0A458',
  cendre: '#5A5148',
  fond: 'transparent',
};

const GRID = 32;

// ---------------------------------------------------------------------------
// Rasterizer
// ---------------------------------------------------------------------------

function px(ctx, x, y, scale, color, brush = 2) {
  ctx.fillStyle = color;
  ctx.fillRect(Math.round(x) * scale, Math.round(y) * scale, brush * scale, brush * scale);
}

function line(ctx, x1, y1, x2, y2, scale, color, brush = 2) {
  let x = x1, y = y1;
  const dx = Math.abs(x2 - x1), dy = Math.abs(y2 - y1);
  const sx = x1 < x2 ? 1 : -1, sy = y1 < y2 ? 1 : -1;
  let err = dx - dy;
  for (let i = 0; i < 200; i++) {
    px(ctx, x, y, scale, color, brush);
    if (Math.round(x) === Math.round(x2) && Math.round(y) === Math.round(y2)) break;
    const e2 = 2 * err;
    if (e2 > -dy) { err -= dy; x += sx; }
    if (e2 < dx) { err += dx; y += sy; }
  }
}

function head(ctx, x, y, scale, color) {
  ctx.fillStyle = color;
  ctx.fillRect((x - 1) * scale, (y - 1) * scale, 3 * scale, 3 * scale);
  ctx.fillRect(x * scale, (y - 2) * scale, scale, scale); // calotte
}

// frame = { b: [[x1,y1,x2,y2],...], e: [...], o: [...], h: [x,y], r: [[x,y,w,h,c]], dur }
export function drawFrame(canvas, frame, scale) {
  const ctx = canvas.getContext('2d');
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  for (const [x, y, w, hh, c] of frame.r || []) {
    ctx.fillStyle = c || PX.cendre;
    ctx.fillRect(x * scale, y * scale, w * scale, hh * scale);
  }
  for (const s of frame.e || []) line(ctx, ...s, scale, PX.cendre);
  for (const s of frame.b || []) line(ctx, ...s, scale, PX.terre);
  for (const s of frame.o || []) line(ctx, ...s, scale, PX.ocre, 1);
  if (frame.h) head(ctx, frame.h[0], frame.h[1], scale, PX.terre);
}

// Monte un sprite animé. Retourne une fonction de nettoyage.
export function mountSprite(container, name, { scale = 4, paused = false } = {}) {
  const sprite = SPRITES[name] || SPRITES.idle;
  const canvas = document.createElement('canvas');
  canvas.width = GRID * scale; canvas.height = GRID * scale;
  canvas.className = 'sprite';
  canvas.style.width = `${GRID * scale / 2}px`;
  canvas.style.height = `${GRID * scale / 2}px`;
  container.append(canvas);

  // Boucle en ping-pong (0,1,2,1,…) : l'aller-retour du mouvement pour le prix
  // de 3 frames — un exercice se lit dans les deux sens.
  const seq = sprite.frames.length >= 3
    ? [...sprite.frames.keys(), ...[...sprite.frames.keys()].slice(1, -1).reverse()]
    : [...sprite.frames.keys()];
  let i = 0, timer = null, stopped = false;
  const reduced = matchMedia?.('(prefers-reduced-motion: reduce)').matches;
  const step = () => {
    if (stopped) return;
    const f = sprite.frames[seq[i]];
    drawFrame(canvas, f, scale);
    const dur = f.dur ?? sprite.dur ?? 700;
    i = (i + 1) % seq.length;
    if (!(reduced || paused || seq.length < 2)) timer = setTimeout(step, dur);
  };
  step();
  return () => { stopped = true; clearTimeout(timer); canvas.remove(); };
}

